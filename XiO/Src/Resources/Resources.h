#pragma once
#include <SFML/Graphics.hpp>
#include "ResourceHolder.h"

////////////////////////////////////////////////////////////
// Przestrzeñ nazw przechowuj¹ca `ID` tekstur.
////////////////////////////////////////////////////////////
namespace Textures {
	enum class ID {
		Background,
		Logo,
		X,
		O
	};
}

////////////////////////////////////////////////////////////
// Przestrzeñ nazw przechowuj¹ca `ID` czcionek.
////////////////////////////////////////////////////////////
namespace Fonts {
	enum class ID {
		Montserrat
	};
}

////////////////////////////////////////////////////////////
// Skróty do typów.
////////////////////////////////////////////////////////////
typedef ResourceHolder<sf::Texture, Textures::ID> TextureHolder;
typedef ResourceHolder<sf::Font, Fonts::ID> FontHolder;

////////////////////////////////////////////////////////////
// Sktruktura przechowuj¹ca wskaŸnik do okna oraz tekstury
// i czionki.
////////////////////////////////////////////////////////////
struct Resources {
	Resources(sf::RenderWindow* window) {
		this->window = window;
	}

	sf::RenderWindow* window;
	TextureHolder textures;
	FontHolder fonts;
};